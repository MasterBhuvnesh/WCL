/**
 * In-memory question bank for the WCL demo exam.
 *
 * Content is a mix of general aptitude, basic computer science, and logical
 * reasoning. Questions are either SCQ (exactly one correct option) or MCQ (one
 * or more correct options).
 *
 * IMPORTANT: the `isCorrect` flag on options is server-only. It MUST NEVER be
 * serialized to any client response. See src/lib/manifest.ts for the safe
 * projection that strips it.
 */

export type QuestionType = "SCQ" | "MCQ";

export interface QuestionOption {
  id: string;
  text: string;
  isCorrect: boolean;
}

export interface Question {
  id: string;
  type: QuestionType;
  text: string;
  marks: number;
  options: QuestionOption[];
}

